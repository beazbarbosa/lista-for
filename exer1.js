let cor = 'vermelho'

switch (cor) {

    case cor = 'vermelho':
        console.log('Pare🤚');
        break;

    case cor = 'amarelo':
        console.log('Atenção⚠️');
        break;

    case cor = 'verde':
        console.log('Siga🫡');
        break;

    default:
        console.log('Cor inválida😒')
}
